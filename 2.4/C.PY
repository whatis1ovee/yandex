n = int(input())
k = 1 #количество чисел в строке 
j = 1 #первый символ в строке 
while k <= n: #пока количество чисел меньше желаемого
    for i in range(k, k + j): #из диапозона
        if i <= n:
            print(i, end=' ') #вывести числа, если они еще меньше желаемого количества
    k += j #количество чисел в строке равно первому числу этой строки 
    j += 1 #с каждой новой строкой количество чисел в ней увеличивается на 1
    print()